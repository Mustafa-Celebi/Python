#'not Operator'

print(not True)
#False

print(not False)
#True

print(not 5 < 6)
#False

print(not 6 < 5)
#True


#'and Operator'

print(True and True)
#True

print(True and False)
#False

print(False and False)
#False

print(5 > 2 and 3 < 1)
#False


#'or Operator'

print(True or False)
#True

print(True or True)
#True

print(False or False)
#False

print(8 == 7 or 6 >= 6)
#True
